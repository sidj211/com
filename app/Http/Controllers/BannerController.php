    public function store(Request $request)
    {
         $input = $request->all(); //4

        if ($file=$request->file('file'))
        {
            //file ka name generate karenge then
            $name = time().$file->getClientOriginalName();
            // then file ko move karenge directory ke andar
            $file->move('images',$name);

            $input['file']=$name;

        }

        Banner::create($input);



    }

